			<h2>॥ गुरुप्रार्थनास्तोत्रम् ॥</h2>
			<div class="panel-body">
				<p>
					पृथ्वीमण्डलराजमाननृपमौल्याभात्किरीटोज्ज्वल-<br>
					द्रत्नाभाञ्चितपादपद्मयुगलश्रीमद्गुरो सत्वरम् ।<br>
					त्वत्पादौकशरण्यमन्दमतयेऽमुष्मै जनाय प्रभो<br>
					हस्तालम्बनमादराद्वितर मां दुःसंसृतेस्तारय ॥ १ ॥
				</p>
				<p>
					श्रीमच्छंकरदेशिकेन्द्ररचिते पीठे जगद्देशिक<br>
					त्वादृक्षाः शिवरूपिणोऽखिलविदो ब्रह्मस्थिता वर्तितुम् ।<br>
					युक्तं कोऽहमतीव मन्दधिषणो बाह्येषु सक्तोऽधुना<br>
					स्थातुं तद्भगवान्भवान्करुणया मामुद्धरत्वञ्जसा ॥ २ ॥
				</p>
				<p>
					नित्यानित्यविवेकविरक्तिशमादिममुमुक्षुताहीनम् ।<br>
					दृष्ट्वा कृपार्द्रदृष्ट्या मुक्त्याध्वनि पथिकमातनुष्व गुरो ॥ ३ ॥
				</p>
				<p>
					ज्ञानेतरेष्टदाने चतुराः कल्पद्रुमणिमुखाः सन्तुः ।<br>
					लोकत्रयेऽपि सत्यं ज्ञानदवस्तु त्वया विना नास्ति ॥ ४ ॥
				</p>
				<p>
					पापीयानिति मत्वा मां यदि लोकैकदेशिक त्यजसि ।<br>
					सर्वजनत्यक्तमिमं को वां परिपालयेत्कृपाजलधे ॥ ५ ॥
				</p>
				<p>
					त्वत्पादाम्बुजयुगलं हित्वा नान्या गतिर्हि मे लोके ।<br>
					इति मत्वा तव चरणौ शरणं प्राप्तोऽस्मि करुणया रक्ष ॥ ६ ॥
				</p>
				<p>
					नास्तिक्यतमसि लोके सर्वजनानां हृदम्बुजातानि ।<br>
					मलिनीकुर्वति धर्मे प्राये नष्टे सदाशिवो भगवान् ॥ ७ ॥
				</p>
				<p>
					गुरुरूपेण धरायामवतीर्याखिलजनानवन्विपदः ।<br>
					धिक्कृतसूर्यमहोभिर्नास्तिक्यतमो विनाश्य दुर्वारम् ॥ ८ ॥
				</p>
				<p>
					कृत्वाऽनेकामानुषकार्याणि जनैः समर्चितो निखिलैः ।<br>
					तद्विदितशंकरांशः लेभे स्वं धाम खलु विसृष्टाङ्गः ॥ ९ ॥
				</p>
				<p>
					तत्तादृङ्भाहिमानं लोकानुग्रहनिबद्धदीक्षमहम् ।<br>
					वैराग्यदाननिरतं शरणं करवाणि देशिकं भक्त्या ॥ १० ॥
				</p>
				<p>
					यन्नैसर्गिककरुणालेशमनुप्राप्य मानवा जगति ।<br>
					स्वीयाचाररताः स्युस्तं देशिकवर्यमादरान्नौमि ॥ ११ ॥
				</p>
				<p>
					श्रीमद्देशिकवर्यान्वन्देऽनुदिनं शिवाभिनवपूर्वान् ।<br>
					सच्चित्सुखरूपान्सद्विद्यां ददतः स्वपादनम्रेभ्यः ॥ १२ ॥
				</p>
