				<h2>देशिकोत्कर्षमञ्जरी </h2>
<div class="panel-body">
<p>
			यद्वक्त्राब्जनिरीक्षणादनुपमः पापी भवेद्धार्मिको<br>
			यद्वाचामृतसेचनादपि जडः स्यात्तत्त्वबोधे गुरुः ।<br>
			यत्सांनिध्यबलादियं वसुमती चाभूत्सतामास्पदं<br>
			स स्वामी मम देशिकस्तदितरो नाम्नाऽपि नाम्नाये ॥
		</p>
<p>
			यो बाल्ये विधिवद्विधूय कुतुकात्कर्माणि कर्मन्दिराट्<br>
			भूत्वा द्राक्समतीतरन्नतजनान्संसारवारांनिधिम् ।<br>
			यस्यामीत्करुणाविशेषमनिशं प्राणिष्वशेषेष्विह<br>
			स स्वामी मम देशिकस्तदितरो नाम्नाऽपि नाम्नाये ॥
		</p>
<p>
			यःस्वानुग्रहकाङ्क्षया निजपदाम्भोजातयुग्मं मुदा<br> 
			संप्राप्तान्विविधार्तिपीडितजनान्प्रीत्याऽनुगृह्णन्स्थितः ।<br>
			यद्वक्त्रं नतकष्टबोधसमये शुष्कं भवेदञ्जसा<br>
			स स्वामी मम देशिकस्तदितरो नाम्नाऽपि नाम्नाये ॥
		</p>
<p>
			भक्तिर्यस्य गुरौ नितान्तमभवद्यस्याः प्रभावात्सदा <br>
			संतुष्टः स्वसमीपमागतवतस्तुष्टान्तरङ्गान्व्यधात् ।<br>
			यस्यासीच्च दृढा मतिः श्रुतिततौ तन्मार्गसंरक्षणे <br>
			स स्वामी मम देशिकस्तदितरो नाम्नाऽपि नाम्नाये ॥
		</p>
<p>
			कीर्तिर्यस्य दिगङ्गनाप्रियसखी मूर्तिश्च हेमाकृतिः<br>
			स्फूर्तिर्देवगुरुं सदोपहसतीवार्ति नतानां जवात् ।<br>
			पूर्तिश्चापि दयारसस्य हरते सूक्तिः सुधास्यन्दिनी<br>
			स स्वामी मम देशिकस्तदितरो नाम्नाऽपि नाम्नाये ॥
		</p>
<p>
			तुङ्गातीरलसत्तपोवनभुवि स्वैरं चरन्नन्वहं<br>
			गङ्गावारिलसज्जटाविलसितं चित्ते मुदा भावयन् ।<br>
			तुङ्गानन्दविधायकः प्रणमतां श्रृङ्गाद्रिवासप्रियः<br>
			स स्वामी मम देशिकस्तदितरो नाम्नाऽपि नाम्नायते ॥
		</p>
<p>
			यस्यास्तिक्यमभूदशेषजनताविस्मापकं भूतले<br>
			यत्कर्माचरणप्रवृत्तिरभवन्मुख्यं प्रमाणं नृणाम् ।<br>
			यत्क्षान्त्या वसुधा मुमोच तरसा सर्वंसहत्वप्रथां<br>
			स स्वामी मम देशिकस्तदितरो नाम्नाऽपि नाम्नायते ॥
		</p>
<p>
			आत्मानात्मविवेचनेऽतिचतुरो मिथ्यात्वमापादय-<br>
			न्द्वैतस्यात्मनि सत्यतामतिदृढं संपादयन् शास्त्रतः ।<br>
			यस्तत्त्वं मनसा विचिन्त्य नितरां लेभेऽनुभूति निजां <br>
			स स्वामी मम देशिकस्तदितरो नाम्नाऽपि नाम्नायते ॥
		</p>
<p>
			शास्त्रार्थान्बहुधा विचार्य विबुधैः साकं विनेयानलं<br>
			संबोध्य श्रुतिभिस्तदङ्गनिचयैस्तर्कैस्तदन्तर्गतैः ।<br>
			निश्चित्य स्वयमात्मतत्त्वमखिलांश्चक्रे सुबोधान्सुखं <br>
			स स्वामी मम देशिकस्तदितरो नाम्नाऽपि नाम्नायते ॥
		</p>
<p>
			उत्पद्यात्मविदग्रजन्मभवने गर्भाष्टमे संस्कृतो<br>
			यः पूर्वार्जितपुण्यपुञ्जविभवैः संप्राप्ततुर्याश्रमः ।<br>
			वैराग्यादिभिरद् भुतैर्गुणगणैः श्रीशंकरार्योऽपरः<br>
			स स्वामी मम देशिकस्तदितरो नाम्नाऽपि नाम्नाये ॥
		</p>
<p>
			यः कल्याणपुरे विशालमतुलं निर्माप्य कंचिन्मठं<br>
			सद्विद्याप्रचयाय तत्र विदुषः साकं महाध्येतृभिः ।<br>
			संस्थाप्याशनवस्त्रवासनिलयान्दत्वाऽऽत्मविद्यामपा-<br>
			स स्वामी मम देशिकस्तदितरो नाम्नाऽपि नाम्नाये ॥
		</p>
<p>
			शुद्धाद्वैतमतप्रचारणकृते वादेन जित्वा तु यां<br>
			श्रृङ्गाद्रौ वरचक्रराजनिलयां चक्रे जगद्देशिकः ।<br>
			यस्तस्यै निरमापयद् दृढतरं कान्तं शिलामन्दिरं<br>
			स स्वामी मम देशिकस्तदितरो नाम्नाऽपि नाम्नाये ॥
		</p>
<p>
			यःश्रीशंकरदेशिकेन्द्रजननस्थानं विदित्वा चिरा-<br>
			त्कृत्वा चात्मवशं मुदा धनचयैर्निर्माप्य सन्मन्दिरम् ।<br>
			तत्र स्थापयति स्म शंकरगुरोर्देव्याश्च मूर्तिं गिरां <br>
			स स्वामी मम देशिकस्तदितरो नाम्नाऽपि नाम्नाये ॥
		</p>
<p>
			सत्यं वाचि दया नतेषु पटुता धर्माध्वसंरक्षणे <br>
			सौजन्यं सुजने भृशं विमुखता दुष्टेषु यस्याभवत् ।<br>
			रक्तिर्ब्रह्मणि निन्दितेषु विषयेष्वासीद्विरक्तिर्दृढा<br>
			स स्वामी मम देशिकस्तदितरो नाम्नाऽपि नाम्नाये ॥
		</p>
<p>
			यस्यासीद् दृढभावना पथि कृतास्वेधोमृदश्मादिभि-<br>
			र्देवानां प्रतिमासु दृष्टिविषयं यातास्वकस्मादपि ।<br>
			पूजास्तोत्रजपादिकर्मसु दृढा तादात्म्यधीरञ्जसा<br>
			स स्वामी मम देशिकस्तदितरो नाम्नाऽपि नाम्नाये ॥
		</p>
