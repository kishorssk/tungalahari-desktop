			<h2>॥ पादावलम्बनस्तुतिः ॥</h2>
			<div class="panel-body">
				<p>
					स्थितेऽनुकम्पामकरन्दपूर्णे<br>
					जगद्गुरोः पादसहस्रपत्रे ।<br>
					किमन्यतो  धावसि चित्तभृङ्ग<br>
					प्रयाहि तत्रैव रमस्व नित्यम् ॥
				</p>
				<p>
					योगिवर्ययमिराजनताङ्घ्रे <br>
					भोगिराजनवनीयचरित्र ।<br>
					रागिभिन्नमनुजाप्यपदाब्ज <br>
					देशिकेन्द्र मम देह्यवलम्बम् ॥ २ ॥
				</p>
				<p>
					यद्वचोमधुरिमापतितेमं<br>
					लोकमेव विससर्ज सुधाऽपि ।<br>
					पादपद्मनतमुक्तिद स त्वं <br>
					देहि देशिक ममाङ्घ्यवलम्बम् ॥ ३ ॥
				</p>
				<p>
					दातृभावमवगम्य महान्तं <br>
					कर्ण आप जनकर्णपथं ते ।<br>
					दारिताखिलदरिद्रजनार्ते<br>
					देहि देशिक ममाङ्घ्यवलम्बम् ॥ ४ ॥
				</p>
				<p>
					शान्तिदान्तिविरतीरपवर्ग-<br>
					प्रापिकाः सुदृढमाशु वितीर्य ।<br>
					जन्ममृत्युभयरोगयुतस्य <br>
					देहि देशिक ममाङ्घ्यवलम्बम् ॥ ५ ॥
				</p>
				<p>
					दर्शनात्किल दुरध्वगमर्त्याः<br>
					यस्य जातु बहवः कृतकृत्याः ।<br>
					सूक्तितस्तु किमु वाच्यमयं त्वं <br>
					देहि देशिक ममाङ्घ्यवलम्बम् ॥ ६ ॥
				</p>
				<p>
					भस्मदानपरिपूरितनम्रा-<br>
					कङ्क्षितार्थ परमात्मविदग्र्य ।<br>
					विस्मयावहतुरीयकयात्र<br>
					देहि देशिक ममाङ्घ्यवलम्बम् ॥ ७ ॥
				</p>
				<p>
					ईहितं वितर सच्चितिसंज्ञ<br>
					मोहितस्य जगदीश्वरशक्त्या ।<br>
					को हितं मम तनोति विना त्वां <br>
					देहि देशिक ममाङ्घ्यवलम्बम् ॥ ८ ॥
				</p>
				<p>
					शंकरार्यकृतसर्वनिबन्ध-<br>
					प्रोक्तिपाटवमतीव वितीर्य ।<br>
					अश्रुतेऽपि परिभाषणदाक्ष्यं<br>
					देहि देशिक ममाङ्घ्यवलम्बम् ॥ ९ ॥
				</p>
				<p>
					व्यासजैमिनिकणादपदाक्षैः<br>
					पाणिनिप्रभृतिभिर्मुनिवर्यैः ।<br>
					प्रोक्तशास्त्रपरिपक्वमनीषां<br>
					देहि देशिक ममाङ्घ्यवलम्बम् ॥ १० ॥
				</p>
				<p>
					मारमत्तगजचण्डमृगेन्द्र<br>
					दारबन्धुममतागकुठार ।<br>
					तारयाशु भववारिनिधेर्मां<br>
					देहि देशिक ममाङ्घ्यवलम्बम् ॥ ११ ॥ 
				</p>
				<p>
					पादधूलि परिपावितलोक<br>
					भेदवादवनदावकृशानो ।<br>
					मोदमान परमात्मनि नित्यं <br>
					देहि देशिक ममाङ्घ्यवलम्बम् ॥ १२ ॥
				</p>
				<p>
					ब्राह्मणत्वमतिदुर्लभमाप्तं <br>
					ह्याश्रमश्च समवापि तुरीयः ।<br>
					नास्ति रागरहितत्वमपीष-<br>
					द्देहि देशिक ममाङ्घ्यवलम्बम् ॥ १३ ॥
				</p>
