<div class="panel-body">
		<p>
			श्रीशङ्कराचार्यवर्य सर्वलोकैकवन्द्यं भजे देशिकेन्द्रम् <br>
			धर्मप्रचारेऽतिदक्षं योगिगोविन्दपादाप्तसन्यासदीक्षम् ।<br>
			दुर्वादिगर्वापनोदं पद्मपादादिशिष्यालिसंसेव्यपादम् ॥ १ ॥
		</p>
<p>
			शङ्काद्रिदम्भोलिलीलं किङ्कराशेषशिष्यालि सन्त्राणशीलम् ।<br>
			बालार्कनीकाशचेलं बोधताशेषवेदान्त गूढार्थजालम् ॥ २ ॥
		</p>
<p>
			रुद्राक्षमालाविभूषं चन्द्रमौलीश्वराराधनावाप्ततोषम् ।<br>
			विद्राविताशेषदोषं भद्रपूगप्रदं भक्तलोकस्य नित्यम् ॥ ३ ॥
		</p>
<p>
			पापाटवीचित्रभानुं ज्ञानदीपेन हार्दं तमो वारयन्तम् ।<br>
			द्वैपायनप्रीतिभाजं सर्वतापापहामाघबोधप्रदं तम् ॥ ४ ॥ 
		</p>
<p>
			राजाधिराजाभिपूज्यं रम्यशृङ्गाद्रिवासैकलोलं यतीड्यम् ।<br>
			राकेन्दुसङ्कासवक्त्रं रत्नगर्भेभवक्त्राङ्घ्रिपूजानुरक्तम् ॥ ५ ॥
		</p>
<p>
