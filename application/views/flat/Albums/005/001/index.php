				<h2>गणेशाष्टकम् </h2>
<div class="panel-body">
		<p>
			सुत्रामपूजितपवित्राङ्घ्रिपद्मयुग पत्रार्चितेभवदन<br>
			मित्राभ साम्बशिवपुत्रारिवर्गकृतवित्रास विघ्नहरण ।<br>
			छत्राभिशोभित तनुत्राभिभूषित परित्राणदीक्षित विभो<br>
			श्रोत्राभिरामगुण पित्रा समोऽसि भव मत्त्राणकर्मनिरतः ॥
		</p>
<p>
			वन्दारुभक्तजनमन्दार पादनतवृन्दारकार्चित महा-<br>
			नन्दानुषङ्गकर निन्दाकरारिगणसन्दाहकाब्जचरण ।<br>
			मन्दाकिनीधर मुकुन्दाभिनन्दित सुकन्दादिभक्ष्यरसिक<br>
			वन्दामहे सुलभ शं दातुमर्हसि मरन्दानुरञ्जित विभो ॥
		</p>
<p>
			पापापनोदकर शापायुधेड्य भवतापार्तशोकहरण<br>
			श्रीपार्वतीतनय कोपार्दितारिगण धूपादितोष्यहृदय ।<br>
			भूपालमौलिनत गोपालपूज्य सुमचापारिपूर्वतनय<br>
			रूपादिमोहकरदीपार्चिषश्शलभमापालयैनमधुना ॥
		</p>
<p>
			हालाहलाशिसुत मालाविभूषित सुशीलावनैकनिरत<br>
			श्रीलाभकारक विनीलालिवृन्दकृतकोलाहलारव विभो ।<br>
			लीलातितुष्ट वरशैलात्मजाङ्कधृत बालाखुवाह विलसत्-<br>
			फालाधुना दलय कालाद्भयं महितवेलाविहीनकरुण ॥
		</p>
<p>
			एकाच्छदन्तमतिशोकातुराङ्घ्रिनतलोकावनैकनिरतं <br>
			नाकालयस्तुतमनेकायुधं विविधशाकादनं सुखकरम् ।<br>
			श्रीकान्तपूज्यमरिहाकारकारमतिभाकारिणं करिमुखं <br>
			राकासुधांशुलसदाकारमाशु नम मा कामनास्तु च परा ॥
		</p>
<p>
			अम्भोजनाभनुतमम्भोजतुल्यपदमम्भोजजातविनुतं <br>
			दम्भोलिधारिनुतकुम्भोद्भवार्च्यचरणाम्भोजयुग्ममतुलम् ।<br>
			अम्भोदवत्सुखकरं भोगिभूषमनिशं भोगदं प्रणमतां<br>
			स्तम्भोरुशुण्डमतुलाम्भोधितुल्यकृपशम्भोः सुतं प्रणमत ॥
		</p>
<p>
			आशाविधानपटुमीशात्मजं सुरगणेशार्चिताङ्घ्रियुगलं <br>
			पाशान्वितं सकलपाशादिबन्धहरमाशापतीडितगुणम् ।<br>
			धीशान्तिदं हृदय कोशान्तरेण भवनाशाय धारय विभुं <br>
			क्लेशापहं सकलदेशार्चितं सुलभमीशानमिक्षुरसिकम् ॥
		</p>
<p>
			धीरतिधीर फलसारादनातिबल घोरारिवर्गभयद<br>
			शूराग्रजात भवभारावमोचनद वीराग्रगण्य सुमुख ।<br>
			माराशुगार्तिकरधाराभयापहर ताराप्रियाञ्चित कृपा-<br>
			वारांनिधे धवलहाराद्य पाहि मदनीराढ्य पादविनतम् ॥
		</p> 
