			<h2>॥ अम्बाष्टकम् ॥</h2>
			<div class="panel-body">
				<p>
					चेटीभवन्निखिलखेटीकदम्बतरुवाटीषु नाकिपटली<br>
					कोटीरचारुतरकोटीमणीकिरणकोटीकरम्बितपदा ।<br>
					पाटीरगन्धकुचशाटी कवित्वपरिपाटीमगाधिपसुता<br>
					घोटीकुलादधिकधाटीमुदारमुखवीटीरसेन तनुताम् ॥
				</p>
				<p>
					कूलातिगामिभयतूलावलिज्वलनकीला निजस्तुतिविधा <br>
					कोलाहलक्षपितकालामरी कलशकीलालपोषणनभाः ।<br>
					स्थूला कुचे जलदनीला कचे कलितलीला कदम्बविपिने <br>
					शूलायुधप्रणतिशीला विभातु हृदि शैलाधिराजतनया ॥
				</p>
				<p>
					यत्राशयो लगति तत्रागजा वसतु कुत्रापि निस्तुलशुका <br>
					सुत्रामकालमुखसत्राशनप्रकरसुत्राणकारिचरणा ।<br>
					छत्रानिलातिरयपत्राभिरामगुणमित्रामरीसमवधूः <br>
					कुत्रासहन्मणिविचित्राकृतिः स्फुरितपुत्रादिदाननिपुणा ॥
				</p>
				<p>
					द्वैपायनप्रभृतिशापायुधत्रिदिवसोपानधूलिचरणा <br>
					पापापहस्वमनुजापानुलीनजनतापापनोदनिपुणा ।<br>
					नीपालया सुरभिधूपालका दुरितकूपादुदञ्चयतु मां <br>
					रूपाधिका शिखरिभूपालवंशमणिदीपायिता भगवती ॥
				</p>
				<p>
					यालीभिरात्मतनुताली सकृत् प्रियकपालीषु खेलति भय-<br>
					व्यालीनकुल्यसितचूलीभरा चरणधूलीलसन्मुिनिवरा ।<br>
					बालीभृति श्रवसि तालीदलं वहति यालीकशोभितिलका<br>
					सालीकरोतु मम काली मनः स्वपदनालीकसेवनविधौ ॥
				</p>
				<p>
					न्यङ्काकरे वपुषि कङ्कादिरक्तपुषि कङ्कादिपक्षिविषये <br>
					त्वङ्कामनामयसि किं कारणं हृदय पङ्कारिमेहि गिरिजाम् ।<br>
					शङ्काशिलानिशितटङ्कायमानपदसङ्काशमानसुमनो-<br>
					झङ्कारिमानततिमङ्कानुपेतशशिसङ्काशिवक्त्रकमलाम् ॥
				</p>
				<p>
					कुम्बावतीसमविडम्बा गलेन नवतुम्बाभवीणसविधा <br>
					शं बाहुलेयशशिबिम्बाधिराममुखसम्बाधितस्तनभरा ।<br>
					अम्बा कुरङ्गमदजम्बालरोचिरिह लम्बालका दिशतु मे <br>
					बिम्बाधरा विनतशम्बायुधादिनकुरम्बा कदम्बविपिने ॥
				</p>
				<p>
					इन्धानकीरमणिबन्धा भवे हृदयबन्धावतीवरसिका<br>
					सन्धावती भुवनसन्धारणेऽप्यमृतसिन्धावुदारनिलया ।<br>
					गन्धानुभानमुहुरन्धालिवीतकचबन्धा समर्पयतु मे <br>
					शं धाम भानुमपि सन्धानमाशुपदसन्धानमप्यगसुता ॥
				</p>
