			<h2>॥ गौर्यष्टकम् ॥</h2>
			<div class="panel-body">
				<p>
					श्रीकामुकाब्जभवपाकारिमुख्यसुरलोकाभिवन्दितपदा <br>
					राकानिशाकरनिराकारिवक्त्ररुचिरा कामपीठनिलया ।<br>
					या कालिका जयपताका स्मरस्य धृतिराकारिणी पुररिपोः <br>
					सा कामितं दिशतु नैकार्चिता विनतशोकापनोदनपरा ॥
				</p>
				<p>
					लाक्षारुणारुणनिजेक्षाक्षणक्षपितरूक्षापदोघचरणाम् <br>
					उक्षाङ्ककेलिरसशिक्षाविचक्षणकटाक्षाङ्कुरां भगवतीम् ।<br>
					अक्षामवक्त्ररुचिभिक्षाटनप्रवणऋक्षाधिपामविरतं <br>
					दाक्षायणीं प्रणतरक्षाविधौ विधृतदीक्षां मतिः श्रयतु मे ॥
				</p>
				<p>
					यामामनन्ति भववामार्धमूर्तिमविरामानुबन्धिकरुणां<br>
					हेमाद्रितुङ्गकुचसीमानमम्बुजवनीमानमाथिनयनाम् ।<br>
					सोमा निजास्यजितसोमा निलिम्पसदसो माननीयचरणा<br>
					भामामणिः प्रचुरकामाय मेऽस्तु हिमधामार्धमस्तकमणिः ॥
				</p>
				<p>
					शाणावलीढसुमबाणास्त्रविभ्रमधुरीणा कटाक्षसरणौ<br>
					शोणाधरा शशविषाणाभमध्यलसितैणाङ्कशेखरसखी ।<br>
					प्राणा बहिः कुलगिरीणामिनस्य रममाणा कदम्बविपिने <br>
					प्रीणातु सा मम रिपूणामनामयपरीणाहपारणचणा ॥
				</p>
				<p>
					या सा सरोरुहविलासापहासिमुखभासा जितामृतकरा<br>
					हासाङ्कुरप्रसरदासायमाननवनासाविभूषणमणिः ।<br>
					व्यासादिमानसनिवासा नमज्जनदयासारपूर्णहृदया <br>
					त्रासापहा सुखविकासाय मेऽस्तु भुवि सा सामजास्यजननी ॥
				</p>
				<p>
					बोधायनादिमुनियूथाशयस्फुरदगाधानुभावचरणा <br>
					वैधात्रसूक्तिमणिसौधा सुधामदतिरोधायिवाङ्मधुरिमा ।<br>
					साधारणेतरदयाधारभूमिरवरोधाङ्गना स्मररिपोः<br>
					बोधात्मिका भुवनबाधापहा भयनिरोधाय मेऽस्तु सततम् ॥
				</p>
				<p>
					स्वाराण्मुखप्रणयिनीराजिमौलिमणिनीराजिताङ्घ्रियुगली <br>
					मारारिलोचनचकोरार्भकेन्दुकरपूरायितस्मितरुचिः ।<br>
					हाराभिरामकुचभारा गिरा विजितकीरा धराधरसुता <br>
					घोरापदुन्मथनधीरा सुखं नतकृपाराशिरादिशतु मे ॥
				</p>
				<p>
					ईहाजयिव्रजविदेहाख्यमुक्तिकरमाहात्म्यसंपदधिका <br>
					देहार्धशङ्करयुता हासनिर्जितसुराहारकान्तिनिचया ।<br>
					स्वाहासखादिमसमूहावनाय रचितोहा सदापि हृदये <br>
					मोहापहा भवतु गेहादिसंपदि महाहानिनाशचतुरा ॥
				</p>
