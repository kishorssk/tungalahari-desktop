			<h2>॥ नटराजाष्टकम् ॥</h2>
			<div class="panel-body">
				<p>
					कुञ्चरचर्मकृताम्बरमम्बुरुहासनमाधवगेयगुणं<br>
					शङ्करमन्तकमानहरं स्मरदाहकलोचनमेणधरम् ।<br>
					साञ्जलियोगिपतञ्जलिसन्नुतमिन्दुकलाधरमब्जमुखं<br>
					मञ्जुलशिञ्जितरञ्जितकुञ्चितवामपदं भज नृत्यपतिम् ॥
				</p>
				<p>
					पिङ्गलतुङ्गजटावलिभासुरगङ्गममङ्गलनाशकरं<br>
					पुङ्गववाहमुमाङ्गधरं रिपुभङ्गकरं सुरलोकनतम् ।<br>
					भृङ्गविनीलगलं गणनाथसुतं भज मानस पापहरं<br>
					मङ्गलदं वररङ्गपतिं भवसङ्गहरं धनराजसखम् ॥
				</p>
				<p>
					पाणिनिसूत्रविनिर्मितिकारणपाणिलसड्डमरूत्थरवं<br>
					माधवनादितमर्दलनिर्गतनादलयोद्धृतवामपदम् ।<br>
					सर्वजगत्प्रलयप्रभुवह्निविराजितपाणिमुमालसितं <br>
					पन्नगभूषणमुन्नतसन्नुतमानम मानस साम्बशिवम् ॥
				</p>
				<p>
					चण्डगुणान्वितमण्डलखण्डनपण्डितमिन्दुकलाकलितं<br>
					दण्डधरान्तकदण्डकरं वरताण्डवमण्डितहेमसभम् ।<br>
					अण्डकराण्डजवाहसखं नम पाण्डवमध्यममोदकरं <br>
					कुण्डलशोभितगण्डतलं मुनिवृन्दनुतं सकलाण्डधरम् ॥
				</p>
				<p>
					व्याघ्रपदानतमुग्रतरासुरविग्रहमर्दिपदाम्बुरुहं <br>
					शक्रमुखामरवर्गमनोहरनृत्यकरं श्रुतिनुत्यगुणम् ।<br>
					व्यग्रतरङ्गितदेवधुनीधृतगर्वहरायतकेशचयं <br>
					भार्गवरावणपूजितमीशमुमारमणं भज शूलधरम् ॥
				</p>
				<p>
					आसुरशक्तिविनाशकरं बहुभासुरकायमनङ्गरिपुं<br>
					भूसुरसेवितपादसरोरुहमीश्वमक्षरमुक्षधृतम् ।<br>
					भास्करशीतकराक्षमनातुरमाश्वरविन्दपदं भज तं <br>
					नश्वरसंसृतिमोहविनाशमहस्करदन्तनिपातकरम् ॥
				</p>
				<p>
					भूतिकरं सितभूतिधरं गतनीतिहरं वरगीतिनुतं <br>
					भक्तियुतोत्तममुक्तिकरं समशक्तियुतं शुभभुक्तिकरम् ।<br>
					भद्रकरोत्तमनामयुतं श्रुतिसामनुतं नम सोमधरं <br>
					स्तुत्यगुणं भज नित्यमगाधभवाम्बुधितारकनृत्यपतिम् ॥
				</p>
				<p>
					शूलधरं भवजालहरं निटिलाग्निधरं जटिलं धवलं<br>
					नीलगलोज्वलमङ्गलसद्गिरिराजसुतामृदुपाणितलम् ।<br>
					शैलकुलाधिपमौलिनतं छलहीनमुपैमि कपालधरं <br>
					कालविषाशमनन्तमिलानुतमद्भुतलास्यकरं गिरिशम् ॥
				</p>
				<p>
					चित्तहरातुलनृत्तपतिप्रियवृत्तकृतोत्तमगीतिमिमां<br>
					प्रातरुमापतिसन्निधिगो यदि गायति भक्तियुतो मनसि ।<br>
					सर्वसुखं भुवि तस्य भवत्यमराधिपदुर्लभमत्यधिकं <br>
					नास्ति पुनर्जनिरेति च धाम स शाम्भवमुत्तममोदकरम् ॥
				</p>
