				<h2>नटेशाष्टकम् </h2>
<div class="panel-body">
		<p>
			सदञ्चितमुदञ्चितनिकुञ्चितपदं झलझलञ्चलितमञ्जुकटकं<br>
			पतञ्जलिदृगञ्जनमनञ्जनमचञ्चलपदं जननभञ्जनकरम् ।<br>
			कदम्बरुचिमम्बरवसं परममम्बुदकदम्बकविडम्बकगलं<br>
			चिदम्बुदमणिं बुधहृदम्बुजरविं परचिदम्बरनटं हृदि भज ॥
		</p>
<p>
			हरं त्रिपुरभञ्जनमनन्तकृतकङ्कणमखण्डदयमन्तरहितं<br>
			विरिञ्चिसुरसंहतिपुरन्दरविचिन्तितपदं तरुणचन्द्रमुकुटम् ।<br>
			परं पदविखण्डितयमं भसितमण्डिततनुं मदनवञ्चनपरं<br>
			चिरन्तनममुं प्रणतसञ्चितनिधिं परचिदम्बरनटं हृदि भज ॥
		</p>
<p>
			अवन्तमखिलं जगदभङ्गगुणतुङ्गममतं धृतविधुं सुरसरित् -<br>
			तरङ्गनिकुरुम्बधृतिलम्पटजटं शमनडम्बरहरं भवहरम् ।<br>
			शिवं दशदिगम्बरविजृम्भितकरं करलसन्मृगशिशुं पशुपतिं<br>
			हरं शशिधनञ्जयपतङ्गनयनं परचिदम्बरनटं हृदि भज ॥
		</p>
<p>
			अनन्तनवरत्नविलसत्कटककिङ्किणिझलञ्झलझलञ्झलरवं<br>
			मुकुन्दविधिहस्तगतमद्दललयध्वनिधिमिद्धिमितनर्तनपदम् ।<br>
			शकुन्तरथबर्हिरथनन्दिमुखदन्तिमुखभृङ्गिरिटिसङ्घनिकटं <br>
			सनन्दसनकप्रमुखवन्दितपदं परचिदम्बरनटं हृदि भज ॥
		</p>
<p>
			अनन्तमहिमं त्रिदशवन्द्यचरणं मुनिहृदन्तरवसन्तममलं <br>
			कबन्धवियदिन्द्ववनिगन्धवहवह्निमुखबन्धुरविमञ्जुवपुषम् ।<br>
			अनन्तविभवं त्रिजगदन्तरमणिं त्रिनयनं त्रिपुरखण्डनपरं<br>
			सनन्दमुनिवन्दितपदं सकरुणं परचिदम्बरनटं हृदि भज ॥
		</p>
<p>
			अचिन्त्यमलिवृन्दरुचिबन्धुरगलस्फुरितकुन्दनिकुरुम्बधवलं<br>
			मुकुन्दसुरवृन्दबलहन्तृकृतवन्दनलसन्तमहिकुण्डलधरम् ।<br>
			अकम्पमनुकम्पितरविं सुजनमङ्गलनिधिं गजहरं पशुपतिं <br>
			धनञ्जयनुतं प्रणतरञ्जनपरं परचिदम्बरनटं हृदि भज ॥
		</p>
<p>
			परं सुरवरं पुरहरं पशुपतिं जनितदन्तिमुखषण्मुखममुं<br>
			मृडं कनकपिङ्गलजटं सनकपङ्कजरविं सुमनसं हिमरुचिम् ।<br>
			असङ्गमनसं जलधिजन्मगरलं कवलयन्तमतुलं गुणनिधिं <br>
			सनन्दवरदं शमितमिन्दुवरदं परचिदम्बरनटं हृदि भज ॥
		</p>
<p>
			अजं क्षितिरथं भुजगपुङ्गवगुणं कनकशृङ्गिधनुषं करलसत् -<br>
			कुरङ्गपृथुटङ्कपरशुं रुचिरकुङ्कुमरुचिं डमरुकञ्च दधतम् ।<br>
			मुकुन्दविशिखं नवमदवन्ध्यफलदं निगमवृन्दतुरगं निरुपमं <br>
			सचण्डिकममुं झटिति संहृतपुरं परचिदम्बरनटं हृदि भज ॥
		</p>
<p>
			अनङ्गपरिपन्थिनमजं क्षितिधुरन्धरमलं करुणयन्तमखिलं <br>
			ज्वलन्तमनलं दधतमन्तरकरिपुं सततमिन्द्रसुरवन्दितपदम् ।<br>
			उदञ्चदरविन्दकुलबन्धुशतबिम्बरुचिसंहतिसुगन्धिवपुषं<br>
			पतञ्जलिनुतं प्रणवपञ्जरशुकं परचिदम्बरनटं हृदि भज ॥
		</p>
<p>
			इति स्तवममुं भुजगपुङ्गवकृतं प्रतिदिनं पठति यः कृतमुखः <br>
			सदःप्रभुपदद्वितयदर्शनफलं सुललितं चरणशृङ्गरहितम् ।<br>
			सरःप्रभवसम्भवहरित्पतिहरिप्रमुखदिव्यनुतशङ्करपदं <br>
			स गच्छति परं न तु जनुर्जलनिधिं परमदुःखजनकं दुरितदम् ॥
		</p>
