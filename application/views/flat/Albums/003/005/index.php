			<h2>॥ श्रीचन्द्रमौलीश्वर वर्णमाला स्तुतिः ॥</h2>
			<div class="panel-body">
				<p>श्रीशात्मभूमुख्यसुरार्चितांघ्रिं श्रीकण्ठशर्वादिपदाभिधेयम् ।<br>
					श्रीशङ्कराचार्यहृदब्जवासं श्रीचन्द्रमौलीशमहं नमामि ॥ १ ॥
				</p>
				<p>चण्डांशुशीतांशुकृशानुनेत्रं चण्डीशमुख्यप्रमथेड्यपादम् ।<br>
					षडास्यनागास्यसुशोभिपार्श्वं श्रीचन्द्रमौलीशमहं नमामि ॥ २ ॥
				</p>
				<p>द्रव्यादिसृष्टिस्थितिनाशहेतुं रव्यादितेजांस्यपि भासयन्तम् ।<br>
					पव्यायुधादिस्तुतवैभवं तं  श्रीचन्द्रमौलीशमहं नमामि ॥ ३ ॥
				</p>
				<p>मौलिस्पुरज्जह्नुसुतासितांशुं व्यालेशसंवेष्टितपाणिपादम् ।<br>
					शूलादिनानायुधशोभमानं श्रीचन्द्रमौलीशमहं नमामि ॥ ४ ॥ 
				</p>
				<p>लीलाविनिर्धूतकृतान्तदर्पं शैलात्मजासंश्रितवामभागम् ।<br>
					शूलाग्रनिर्भिन्नसुरारिसंघं श्रीचन्द्रमौलीशमहं नमामि ॥ ५ ॥
				</p>
				<p>शतैः श्रुतीनां परिगीयमानं यतैर्मुनीन्द्रैः परिसेव्यमानम् ।<br>
					नतैः सुरेन्द्रैरभिपूज्यमानं श्रीचन्द्रमौलीशमहं नमामि ॥ ६ ॥
				</p>
				<p>मत्तेभकृत्या परिशोभिताङ्गं चित्ते यतीनां सततं वसन्तम् ।<br>
					वित्तेशमुख्यैः परिवेष्टितं तं श्रीचन्द्रमौलीशमहं नमामि ॥ ७ ॥
				</p>
				<p>हंसोत्तमैः चेतसि चिन्त्यमानं संसारपाथोनिधिकर्णधारम् ।<br>
					तं सामगानप्रियमष्टमूर्तिं श्रीचन्द्रमौलीशमहं नमामि ॥ ८ ॥
				</p>
				<p>नताघहं नित्यचिदेकरूपं सतां गतिं सत्यसुखस्वरूपम् ।<br>
					हतान्धकं हृद्यपराक्रमं तं श्रीचन्द्रमौलीशमहं नमामि ॥ ९ ॥
				</p>
				<p>मायागिगं वीतभयं विनिद्रं मोहान्तकं मृत्युहरं महेशम् ।<br>
					फालानलं नीलगलं कृपालुं श्रीचन्द्रमौलीशमहं नमामि ॥ १० ॥
				</p>
				<p>मित्रं हि यस्याखिलशेवधीशः पुत्रश्च विघ्नौघविभेददक्षः ।<br>
					पात्रं कृपायाश्च समस्तलोकः श्रीचन्द्रमौलीशमहं नमामि ॥ ११ ॥
				</p>
