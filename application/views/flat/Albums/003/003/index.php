			<h2>॥ श्रीशङ्कराचार्यस्तवः ॥</h2>
			<div class="panel-body">
				<p>मुदा करेण पुस्तकं दधानमीशरूपिणं<br>
					तथाऽपरेण मुद्रिकां नमत्तमोविनाशिनीम् ।<br>
					कुसुम्भवाससावृतं विभूतिभासिफालकं<br>
					नताऽघनाशने रतं नमामि शङ्करं गुरुम् ॥ १ ॥
				</p>
				<p>पराशरात्मजप्रियं पवित्रितक्षमातलं<br>
					पुराणसारवेदिनं सनन्दनादिसेवितम् ।<br>
					प्रसन्नवक्त्रपङ्कजं प्रपन्नलोकरक्षकम् <br>
					प्रकाशिताद्वितीयतत्त्वमाश्रयामि देशिकम् ॥ २ ॥
				</p>
				<p>सुधांशुशेखरार्चकं सुधीन्द्रसेव्यपादुकं<br>
					सुतादिमोहनाशकं सुशान्तिदान्तिदायकम् ।<br>
					समस्तवेदपारगं सहस्रसूर्यभासुरं<br>
					समाहिताखिलेन्द्रियं सदा भजामि शङ्करम् ॥ ३ ॥
				</p>
				<p>यमीन्द्रचक्रवर्तिनं यमादियोगवेदिनं<br>
					यथार्थत्त्वबोधकं यमान्तकात्मजार्चकम् <br>
					यमेव मुक्तिकांक्षया समाश्रयन्ति सज्जनाः<br>
					नमाम्यहं सदा गुरुं तमेव शङ्कराभिधम् ॥ ४ ॥
				</p>
				<p>स्वबाल्य एव निर्भरं य आत्मनो दयालुतां <br>
					दरिद्रविप्रमन्दिरे सुवर्णवृष्टिमानयन् ।<br>
					प्रदर्श्य विस्मयाम्बुधौ न्यमज्जयत् समाञ्जनान्<br> 
					स एव शङ्करस्सदा जगद्गुरुर्गतिर्मम ॥ ५ ॥
				</p>
				<p>यदीयपुण्यजन्मना प्रसिद्धिमाप कालटी<br>
					यदीयशिष्यतां व्रजन् स तोटकोऽपि पप्रथे ।<br>
					य एव सर्वदेहिनां विमुक्तिमार्गदर्शकः <br>
					नराकृतिं सदाशिवं तमाश्रयामि सद्गुरुम् ॥ ६ ॥
				</p>
				<p>सनातनस्य वर्त्मनः सदैव पालनाय यः<br>
					चतुर्दिशासु सन्मठान् चकार लोकविश्रुतान् ।<br>
					विभाण्डकात्मजाश्रमादिसुस्थलेषु पावनान् <br>
					तमेव लोकशङ्करं नमामि शङ्करं गुरुम् ॥ ७ ॥
				</p>
				<p>यदीयहस्तवारिजातसुप्रतिष्टिता सती<br>
					प्रसिद्धश्रृभूधरे सदा प्रशान्तिभासुरे ।<br>
					स्वभक्तपालनव्रता विराजते हि शारदा <br>
					स शङ्करः कृपानिधिः करोतु मामनेनसम् ॥ ८ ॥ 
				</p>
				<p>इमं स्तवं जगद्गुरोर्गुणानुवर्णनात्मकं<br>
					समादरेण यः पठेदनन्यभक्तिसंयुतः ।<br>
					समाप्नुयात् समीहितं मनोरथं नरोऽचिरात् <br>
					दयानिधेस्स शङ्करस्य सद्गुरोः प्रसादतः ॥ ९ ॥
				</p>
