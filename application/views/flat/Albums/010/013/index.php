			<h2>॥ उपमन्युकृतशिवस्तोत्रम् ॥</h2>
			<div class="panel-body">
				<p>
					जय शङ्कर पार्वतीपते <br>
					मृड शम्भो शशिखण्डमण्डन ।<br>
					मदनान्तक भक्तवत्सल<br>
					प्रियकैलास दयासुधाम्बुधे ॥ १ ॥
				</p>
				<p>
					सदुपायकथास्वपण्डितो<br>
					हृदये दुःखशरेण खण्डितः ।<br>
					शशिखण्डशिखण्डमण्डनं <br>
					शरणं यामि शरणयमीश्वरम् ॥ २ ॥
				</p>
				<p>
					महतः परितः प्रसर्पत-<br>
					स्तमसो दर्शनभेदिनो भिदे ।<br>
					दिननाथ इव स्वतेजसा <br>
					हृदयव्योम्नि मनागुदेहि नः ॥ ३ ॥
				</p>
				<p>
					न वयं तव चर्मचक्षुषा <br>
					पदवीमप्युपवीक्षितुं क्षमाः ।<br>
					कृपयाऽभयदेन चक्षुषा <br>
					सकलेनेश विलोकयाशुनः ॥ ४ ॥
				</p>
				<p>
					त्वदनुस्मृतिरेवपावनी<br>
					स्मृतियुक्ता नहि वक्तुमीश सा ।<br>
					मधुरं हि पयः स्वभावतो<br>
					ननु किट्टक्सितशर्करान्वितम् ॥ ५ ॥
				</p>
				<p>
					सविषोऽप्यमृतायते भवान् <br>
					शवमुण्डाभरणोऽपि पावनः ।<br>
					भव एव भवान्तकस्सतां<br>
					समदृष्टिर्विषमेक्षणोऽपि सन् ॥ ६ ॥
				</p>
				<p>
					अपि शूलधरो निरामयो<br>
					दृढवैराग्यरतोऽपि रागवान् ।<br>
					अपि भैक्षचरो महेश्वर-<br>
					श्चरितं चित्रमिदं हि ते प्रभो ॥ ७ ॥
				</p>
				<p>
					वितरत्यभिवाञ्छितं दृशा <br>
					परिदृष्टः किल कल्पपादपः ।<br>
					हृदये स्मृत एव धीमते<br>
					नमतेऽभीष्टफलप्रदो भवान् ॥ ८  ॥ 
				</p>
				<p>
					सहसैव भुजङ्गपाशवन्<br>
					विनिगुह्णाति न यावदन्तकः ।<br>
					अभयं कुरु तावदाशु मे <br>
					गतजीवस्य पुनः किमौषधैः ॥ ९ ॥
				</p>
				<p>
					सविषैरिव भीमपन्नगै-<br>
					र्विषयैरेभिरलं परिक्षतम् ।<br>
					अमृतैरिव संभ्रमेण मा-<br>
					मभिषिञ्चाशु दयावलोकनैः ॥ १० ॥
				</p>
				<p>
					मुनयो बहवोऽद्य धन्यतां<br>
					गमिताः स्वाभिमतार्थदर्शिनः ।<br>
					करुणाकर येन तेन मा-<br>
					मवसन्नं ननु पश्य चक्षुषा ॥ ११ ॥
				</p>
				<p>
					प्रणमाम्यथ यामि चापरं <br>
					शरणं कं कृपणाभयप्रदम् ।<br>
					विरहीव विभो प्रियामयं <br>
					परिपश्यामि भवन्मयं जगत् ॥ १२ ॥
				</p>
				<p>
					बहवो भवतानुकम्पिताः<br>
					किमितीशान न मानुकम्पसे ।<br>
					दधता किमु मन्दराचलं <br>
					परमाणुः कमठेन दुर्धरः ॥ १३ ॥
				</p>
				<p>
					अशुचिं यदि मानुमन्यसे <br>
					किमिदं मूर्ध्नि कपालदाम ते ।<br>
					उत शाठ्यमसाधुसङ्गिनं<br>
					विषलक्ष्मासि न किं द्विजिह्वधृक् ॥ १४ ॥
				</p>
				<p>
					क्व दृशं विदधामि किं करो-<br>
					म्यनुतिष्ठामि कथं भयाकुलः ।<br>
					क्व नु तिष्ठसि रक्ष रक्ष मा-<br>
					मयि शम्भो शरणागतोऽस्मि ते ॥ १५ ॥
				</p>
				<p>
					विलुठाम्यवनौ किमाकुलः<br>
					किमुरो हन्मि शिरश्छिनद्मि वा ।<br>
					किमु रोदिमि रारटिमि किं <br>
					कृपणं मां न यदीक्षसे प्रभो ॥ १६ ॥
				</p>
				<p>
					शिव सर्वग शर्व शर्मद<br>
					प्रणतो देव दयां कुरुष्व मे ।<br>
					नम ईश्वर नाथ दिक्पते<br>
					पुनरेवेश नमो नमोऽस्तु ते ॥ १७ ॥
				</p>
				<p>
					शरणं तरुणेन्दुशेखरः<br>
					शरणं मे गिरिराजकन्यका ।<br>
					शरणं पुनरेव तावुभौ<br>
					शरणं नान्यदुपैमि दैवतम् ॥ १८ ॥
				</p>
				<p>
					उपमन्युकृतं स्तवोत्तमं<br>
					जपतश्शम्भुसमीपवर्तिनः ।<br>
					अभिवाञ्छितभाग्यसम्पदः <br>
					परमायुः प्रददाति शङ्करः ॥ १९ ॥
				</p>
				<p>
					उपमन्युकृतं स्तवोत्तमं <br>
					प्रजपेद्यस्तु शिवस्य सन्निधौ ।<br>
					शिवलोकमवाप्य सोऽचिरा-<br>
					त्सह तेनैव शिवेन मोदते ॥ २० ॥
				</p>
