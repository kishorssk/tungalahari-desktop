				<h2>सन्तः पुत्राः </h2>
<div class="panel-body">
		<p>
			सन्तः पुत्राः सुहृद उत वा सत्कळत्रं सुगेहं<br>
			वित्ताधीशप्रतिमवसुमान् बोभवीतु प्रकामम् ।<br>
			आशास्वास्ताममृतकिरणस्पर्धिकीर्तिच्छटा वा <br>
			सर्वं व्यर्थं मरणसमये साम्ब एकः सहायः ॥ १ ॥
		</p>
<p>
			वादे सर्वानपि विजयतां सत्सभायां नृपाग्रे <br>
			भोगान् सर्वाननुभवतु वा दैवतैरप्यलभ्यान् ।<br>
			भूमौ नीरे वियति चरितु वर्ततां योगशक्तिः <br>
			सर्वं व्यर्थं मरणसमये साम्ब एकः सहायः ॥ २ ॥
		</p>
<p>
			रूपं वास्तां कुसुमविशिखाखर्वगर्वापहारि<br>
			शौर्यं वास्ताममरपतिसंक्षोभदक्षं नितान्तम् ।<br>
			पृथ्वीपालप्रवरमकुटाघट्टनं स्यात्पदे वा<br>
			सर्वं व्यर्थं मरणसमये साम्ब एकः सहायः  ॥ ३ ॥
		</p>
<p>
			गेहे सन्तु प्रवरभिषजस्सर्वरोगापनोदाः<br>
			देशे देशे बहुधनयुता बन्धवस्सन्तु कामम् ।<br>
			सर्वे लोका अपि वचनतो दासवत्कर्म कुर्युः <br>
			सर्वं व्यर्थं मरणसमये साम्ब एकः सहायः ॥ ४ ॥
		</p>
<p>
			अध्यास्तां वा सुमणिखचितं दिव्यपारीणपीठं<br>
			हस्त्यश्वाद्यैरपि परिवृतो द्वारदेशोऽस्तु कामम् ।<br>
			भूष्यन्तां वाभरणनिवहैरङ्गकान्यर्घशून्यैः <br>
			सर्वं व्यर्थं मरणसमये साम्ब एकः सहायः ॥ ५ ॥
		</p>
<p>
			धत्तां मूर्ध्नि प्रवरमणिभिर्जुष्टदीव्यत्किरीटं<br>
			वस्तां देहं विविधवसनैर्हेमसूत्रावबद्धैः ।<br>
			आरुह्यासौ विचरतु भुवं तिर्यगान्दोलिकां वा<br>
			सर्वं व्यर्थं मरणसमये साम्ब एकः सहायः ॥ ६ ॥
		</p>
<p>
			सर्वाशान्तप्रकटितरवैर्वन्दिभिः स्तूयतां वा<br>
			भेरीढक्काप्रमुखबिरुदं दिक्षु दन्ध्वन्यतां वा ।<br>
			पृथ्वीं सर्वामवतु रिपुभिः क्रान्तपादाग्रपीठः <br>
			सर्वं व्यर्थं मरणसमये साम्ब एकः सहायः  ॥ ७ ॥
		</p>
<p>
			हृद्यां पद्यावलिमपि करोत्वर्थचित्रं सुकाव्यं <br>
			षट्छास्त्रेष्वप्यमितधिषणो ग्रन्थसन्दोहकर्ता ।<br>
			सर्वेषां स्यादमितहृदयानन्ददो वाङ्मुखैर्वा<br>
			सर्वं व्यर्थं मरणसमये साम्ब एकः सहायः ॥ ८ ॥
		</p>
