			<h3>॥श्रीकमलजदयिताष्टकम्॥</h3>
			<div class="panel-body">
				<p>शृङ्गक्ष्माभृन्निवासे शुकमुखमुनिभिः सेव्यमानाङ्घ्रिपद्मे <br/>
				स्वाङ्गच्छायाविधूतामृतकरसुरराड्वाहने वाक्सवित्रि । <br/>
				शम्भुश्रीनाथमुख्यामरवरनिकरैर्मोदतः पूज्यमाने <br/>
				विद्यां शुद्धां च बुद्धिं कमलजदयिते सत्वरं देहि मह्यम् ॥ १ ॥</p>

				<p>कल्यादौ पार्वतीशः प्रवरसुरगणप्रार्थितः श्रौतवर्त्म <br/>
				प्राबल्यं नेतुकामो यतिवरवपुषागत्य यां शृङ्गशैले ।<br/>
				संस्थाप्यार्चां प्रचक्रे बहुविधनुतिभिः सा त्वमिन्द्वर्धचूडा <br/>
				विद्यां शुद्धां च बुद्धिं कमलजदयिते सत्वरं देहि मह्यम् ॥ २ ॥</p>

				<p>पापौघं ध्वंसयित्वा बहुजनिरचितं किं च पुण्यालिमारा <br/>
				त्संपाद्यास्तिक्यबुद्धिं श्रुतिगुरुवचनेष्वादरं भक्तिदार्ढ्यम् ।<br/>
				देवाचार्यद्विजातिष्वपि मनुनिवहे तावकीने नितान्तं<br/>
				विद्यां शुद्धां च बुद्धिं कमलजदयिते सत्वरं देहि मह्यम् ॥ ३ ॥</p>

				<p>विद्यामुद्राक्षमालामृतघटविलसत्पाणिपाथोजजाले <br/>
				विद्यादानप्रवीणे जडबधिरमुखेभ्योऽपि शीघ्रं नतेभ्यः । <br/>
				कामादीनान्तरान्मत्सहजरिपुवरान् देवि निर्मूल्य वेगा- <br/>
				द्विद्यां शुद्धां च बुद्धिं कमलजदयिते सत्वरं देहि मह्यम् ॥ ४ ॥</p>

				<p>कर्मस्वात्मोचितेषु स्थिरतरधिषणां देहदार्ढ्यं तदर्थं <br/>
				दीर्घं चायुर्यशश्च त्रिभुवनविदितं पापमार्गाद्विरक्तिम् ।<br/>
				सत्सङ्गं सत्कथायाः श्रवणमपि सदा देवि दत्त्वा कृपाब्धे <br/>
				विद्यां शुद्धां च बुद्धिं कमलजदयिते सत्वरं देहि मह्यम् ॥ ५ ॥</p>

				<p>मातस्त्वत्पादपद्मं न विविधकुसुमैः पूजितं जातु भक्त्या<br/>
				गातुं नैवाहमीशे जडमतिरलसस्त्वद्गुणान् दिव्यपद्यैः ।<br/>
				मूके सेवाविहीनेऽप्यनुपमकरुणामर्भकेऽम्बेव कृत्वा<br/>
				विद्यां शुद्धां च बुद्धिं कमलजदयिते सत्वरं देहि मह्यम् ॥ ६ ॥</p>

				<p>शान्त्याद्याः संपदो मे वितर शुभकरीर्नित्यतद्भिन्नबोधं<br/> 
				वैराग्यं मोक्षवाञ्छामपि लघु कलय श्रीशिवासेव्यमाने ।<br/>
				विद्यातीर्थादियोगिप्रवरकरसरोजातसंपूजिताङ्घ्रे<br/>
				विद्यां शुद्धां च बुद्धिं कमलजदयिते सत्वरं देहि मह्यम् ॥ ७ ॥</p>

				<p>सच्चिद्रूपात्मनो मे श्रुतिमनननिदिध्यासनान्याशु मातः <br/>
